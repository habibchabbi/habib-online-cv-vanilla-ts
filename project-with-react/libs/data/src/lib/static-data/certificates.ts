const CERTIFICATES_BY_LANGUAGE = {
  en: CERTIFICATES_EN,
  fr: CERTIFICATES_FR,
  de: CERTIFICATES_DE,
  nl: CERTIFICATES_NL,
  ar: CERTIFICATES_AR,
} as const;

export type CertificatesLanguage = keyof typeof CERTIFICATES_BY_LANGUAGE;

export const getCertificatesByLanguage = (
  language: string,
): CertificateType[] => {
  if (
    language &&
    Object.prototype.hasOwnProperty.call(CERTIFICATES_BY_LANGUAGE, language)
  ) {
    return CERTIFICATES_BY_LANGUAGE[language as CertificatesLanguage];
  }

  return CERTIFICATES_EN;
};

export const CERTIFICATES = CERTIFICATES_EN;


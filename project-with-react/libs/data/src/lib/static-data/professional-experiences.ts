const PROFESSIONAL_EXPERIENCES_BY_LANGUAGE = {
  en: PROFESSIONAL_EXPERIENCES_EN,
  fr: PROFESSIONAL_EXPERIENCES_FR,
  de: PROFESSIONAL_EXPERIENCES_DE,
  nl: PROFESSIONAL_EXPERIENCES_NL,
  ar: PROFESSIONAL_EXPERIENCES_AR,
} as const;

export type ProfessionalExperienceLanguage = keyof typeof PROFESSIONAL_EXPERIENCES_BY_LANGUAGE;

export const getProfessionalExperiencesByLanguage = (
  language: string,
): ProfessionalExperienceType[] => {
  if (
    language &&
    Object.prototype.hasOwnProperty.call(
      PROFESSIONAL_EXPERIENCES_BY_LANGUAGE,
      language,
    )
  ) {
    return PROFESSIONAL_EXPERIENCES_BY_LANGUAGE[
      language as ProfessionalExperienceLanguage
    ];
  }

  return PROFESSIONAL_EXPERIENCES_EN;
};

export const PROFESSIONAL_EXPERIENCES = PROFESSIONAL_EXPERIENCES_EN;
